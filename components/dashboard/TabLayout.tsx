'use client';

import { useState, ReactNode } from 'react';

export interface TabItem {
  id: string;
  label: string;
  icon: string; // SVG path
}

interface TabLayoutProps {
  tabs: TabItem[];
  defaultTab?: string;
  children: (activeTab: string) => ReactNode;
  className?: string;
}

export default function TabLayout({ tabs, defaultTab, children, className = '' }: TabLayoutProps) {
  const [activeTab, setActiveTab] = useState(defaultTab || tabs[0]?.id || '');

  return (
    <div className={`flex flex-col md:flex-row gap-8 ${className}`}>
      {/* Sidebar Tabs */}
      <div className="w-full md:w-64 space-y-1 flex-shrink-0">
        {tabs.map((tab) => (
          <button
            key={tab.id}
            onClick={() => setActiveTab(tab.id)}
            className={`w-full flex items-center gap-3 px-4 py-3 rounded-xl text-sm font-bold transition-all ${
              activeTab === tab.id
                ? 'bg-gradient-to-r from-[#c9a227] to-[#f0d78c] text-[#1a1a1d] shadow-md shadow-[#c9a227]/20'
                : 'text-gray-400 hover:text-[#1a1a1d] hover:bg-amber-50'
            }`}
          >
            <svg className="w-5 h-5" fill="none" stroke="currentColor" viewBox="0 0 24 24">
              <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d={tab.icon} />
            </svg>
            {tab.label}
          </button>
        ))}
      </div>

      {/* Content Area */}
      <div className="flex-1 bg-white rounded-3xl border border-gray-100 shadow-sm overflow-hidden min-h-[600px]">
        {children(activeTab)}
      </div>
    </div>
  );
}

